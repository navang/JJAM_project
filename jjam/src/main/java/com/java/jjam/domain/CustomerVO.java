package com.java.jjam.domain;

public class CustomerVO { //회원
	private String c_id;  //회원아이디
	private String c_name; //회원이름
	private String c_tel; //회원전화번호
	private String c_pw; //회원비밀번호
	
	
	
	
	//getter, setter
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_tel() {
		return c_tel;
	}
	public void setC_tel(String c_tel) {
		this.c_tel = c_tel;
	}
	public String getC_pw() {
		return c_pw;
	}
	public void setC_pw(String c_pw) {
		this.c_pw = c_pw;
	}
	
	
	

	
}
